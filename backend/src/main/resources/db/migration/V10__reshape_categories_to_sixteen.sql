UPDATE categories
SET
  name = CASE id
    WHEN 1 THEN 'Красота и уелнес'
    WHEN 2 THEN 'Фризьорски и бръснарски услуги'
    WHEN 3 THEN 'Автомобилни услуги'
    WHEN 4 THEN 'Домашни ремонти и майстори'
    WHEN 5 THEN 'Почистване и хигиенни услуги'
    WHEN 6 THEN 'Медицина и здраве'
    WHEN 7 THEN 'Фитнес, йога и спорт'
    WHEN 8 THEN 'Образование, уроци и курсове'
    WHEN 9 THEN 'Фото, видео и творчески услуги'
    WHEN 10 THEN 'Ветеринарни услуги'
    WHEN 11 THEN 'Финансови и счетоводни консултации'
    WHEN 12 THEN 'Транспортни и шофьорски услуги'
    WHEN 13 THEN 'Правни услуги'
    ELSE name
  END,
  description = CASE id
    WHEN 1 THEN 'Козметика, маникюр, естетични процедури и уелнес услуги.'
    WHEN 2 THEN 'Подстригване, стилизиране, боядисване, брада и бръснарски грижи.'
    WHEN 3 THEN 'Сервизи, диагностика, гуми, масла и поддръжка на автомобили.'
    WHEN 4 THEN 'Майстори, ремонти, монтажи и техническа помощ за дома или офиса.'
    WHEN 5 THEN 'Професионално почистване, дезинфекция и хигиенни услуги.'
    WHEN 6 THEN 'Прегледи, терапии, рехабилитация и грижа за здравето.'
    WHEN 7 THEN 'Персонални тренировки, йога, групови занимания и активен начин на живот.'
    WHEN 8 THEN 'Езици, частни уроци, курсове и професионално обучение.'
    WHEN 9 THEN 'Фотография, видеозаснемане, монтаж и творчески проекти.'
    WHEN 10 THEN 'Ветеринарни прегледи, грижа, терапии и услуги за животни.'
    WHEN 11 THEN 'Счетоводство, данъчни консултации и финансова помощ за хора и бизнес.'
    WHEN 12 THEN 'Шофьорски уроци, транспортни консултации и свързани услуги.'
    WHEN 13 THEN 'Юридически консултации, документи и правно съдействие.'
    ELSE description
  END
WHERE id BETWEEN 1 AND 13;

